/********************************************************************************
*@brief   None
*@details None
*********************************************************************************
*@param[in]  None
*@param[out] None
*@note       None
*@return     None
********************************************************************************/
void led_on(uint8_t led_no)
{
  uint32_t *pGpiodDataReg= (uint32_t*) 0x40020C14;
  *pGpiodDataReg|= (1 << led_no);

}

/********************************************************************************
*@brief   None
*@details None
*********************************************************************************
*@param[in]  None
*@param[out] None
*@note       None
*@return     None
********************************************************************************/
void led_off(uint8_t led_no)
{
  uint32_t *pGpiodDataReg= (uint32_t*) 0x40020C14;
  *pGpiodDataReg&= ~(1 << led_no);

}

